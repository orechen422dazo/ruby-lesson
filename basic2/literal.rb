# 数値の１２３、文字列などをリテラルという。
# Rubyで使える例

# 数字
puts 123

# 文字列
puts "Hi"

# 配列
puts [1, 2, 3]

# ハッシュ
puts {'ja' => '81'}

# 変数名 = 式や値
num = 123
puts num

greet = "Hi"
puts greet

is_done = true
puts is_done

n = 10 * 2
puts n

_private = "private"
puts _private

tax_10 = "10%"
puts tax_10

1number = 1
puts 1number